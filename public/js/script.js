function displayBooks(bookArray){

let data="";

bookArray.forEach(book=>{

data += `

<div class="book">

<img src="${book.image}">

<h3>${book.name}</h3>

<p>₹${book.price}</p>

<p>⭐⭐⭐⭐⭐</p>

<button class="buy"
onclick='addToCart(${JSON.stringify(book)})'>
Add To Cart
</button>

</div>

`;

});

document.getElementById("books").innerHTML = data;

}
